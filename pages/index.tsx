import {
  Heading,
  Stack,
  Center,
} from "@chakra-ui/layout";
import {
  Button,
  Alert,
  AlertIcon,
} from "@chakra-ui/react";
import Head from "next/head";
import axios from "../constants/axios";
import Link from "next/link";
import { Spot } from "../types/spot";
import { FiArrowRight } from "react-icons/fi";
import SpotCard from "../src/components/SpotCard";

export const getServerSideProps = async () => {
  const res = await axios.get("/api/spots/");
  const spots = res.data.spots;
  return {
    props: {
      spots,
    },
  };
};

type Props = {
  spots: Spot[];
};

const Home: React.FC<Props> = ({ spots }) => {
  console.log(spots);
  return (
    <div>
      <Head>
        <title>SkateSpot</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>

            <Alert status="warning">
              <AlertIcon />
              このサービスはβ版です。投稿したデータは削除される可能性があります。
            </Alert>
        <Center>
          <Stack p={8} w="lg">
            <Heading>新着のスポット</Heading>
            {spots.map((spot) => {
              return <SpotCard key={spot.id} spot={spot} />;
            })}
            <Link href="/spot">
              <Button rightIcon={<FiArrowRight />}>もっと見る</Button>
            </Link>
          </Stack>
        </Center>
      </main>
    </div>
  );
};
export default Home;
